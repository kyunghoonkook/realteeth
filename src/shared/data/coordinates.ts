// 좌표 찾기 함수
export function findCoordinates(
  city: string,
  district?: string
): RegionCoordinate | null {
  // 구/군 먼저 검색 (더 정확한 위치)
  if (district) {
    const districtCoord = districtCoordinates[district];
    if (districtCoord) {
      return districtCoord;
    }
  }

  // 시/도 검색
  const provinceCoord = provinceCoordinates[city];
  if (provinceCoord) {
    return provinceCoord;
  }

  return null;
}
